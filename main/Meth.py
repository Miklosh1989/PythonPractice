def sum(rubles, penny):
    print(rubles + float(penny) / 100)
def subtraction(rubles, penny):
    print(rubles - float(penny) / 100)
def division(rubles, penny):
    print(rubles / penny)
def divByFraction(rubles, penny):
    print(rubles / float(penny) / 100)
def multByFraction(rubles, penny):
    print(rubles * float(penny) / 100)
def main(rubles, penny):
    sum(rubles, penny)
    subtraction(rubles, penny)
    division(rubles, penny)
    divByFraction(rubles, penny)
    multByFraction(rubles, penny)